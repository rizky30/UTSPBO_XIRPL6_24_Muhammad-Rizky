    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd") ;
        String tanggal = dateformat.format(tfTanggal.getDate()) ;
        if("".equals(tanggal) || "".equals(tfKasir.getText()) || "".equals(tfMenu.getText()) || "".equals(tfHarga.getText())){
            JOptionPane.showMessageDialog(this, "Harap lengkapi Data","Error", JOptionPane.WARNING_MESSAGE);

        }else{

            harga = Integer.parseInt(tfHarga.getText()) ;
            if ( harga > 200000)
            {
                diskon = (10 * harga) / 100 ;
                total = harga - diskon ;

            }else{

                diskon = 0 ;
                total = harga ;

            }

            String SQL = "Insert Into tb_transaksi(id, nama_kasir, tgl_beli, nama_menu, harga, diskon, total)"+"Values("+null+",'"+tfKasir.getText()+"','"+tanggal+"','"+tfMenu.getText()+"','"+harga+"', '"+diskon+"', '"+total+"')" ;
            int status = KoneksiDB.execute(SQL) ;
            if(status == 1){

                JOptionPane.showMessageDialog(this, "Berhasil Ditambahkan","sukses",JOptionPane.INFORMATION_MESSAGE);
                selectdata();
            }else{

                JOptionPane.showMessageDialog(this, "Gagal Ditambahkan","gagal",JOptionPane.WARNING_MESSAGE);

            }

        }
    }//GEN-LAST:event_btnAddActionPerformed

