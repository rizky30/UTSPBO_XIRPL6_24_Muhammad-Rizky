    public void selectdata() {
        //To change body of generated methods, choose Tools | Templates.
        String kolom[] = {"No","nama_kasir","tgl_beli","nama_menu", "harga", "diskon", "total"} ;
        DefaultTableModel dtm = new DefaultTableModel(null, kolom) ;
        String SQL = "Select * From tb_transaksi " ;
        ResultSet rs = KoneksiDB.executeQuery(SQL) ;
        try {
            
            while(rs.next()){
                String No = rs.getString(1) ;
                String nama_kasir = rs.getString(2) ;
                String tgl_beli = rs.getString(3) ;
                String nama_menu = rs.getString(4) ;
                String harga = rs.getString(5) ;
                String diskon = rs.getString(6) ;
                String total = rs.getString(7) ;
                String data[] = {No ,nama_kasir, tgl_beli, nama_menu, harga, diskon, total} ;
                dtm.addRow(data);
            }
            
        }catch(SQLException ex){
            
            Logger.getLogger(frMain.class.getName()).log(Level.SEVERE, null, ex) ;
        }
        tbData.setModel(dtm);
    }
